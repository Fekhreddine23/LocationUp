// -- This is a parent command --
Cypress.Commands.add('login', (username: string, password: string) => {
  cy.visit('/login')
  cy.get('input[name="username"]').type(username)
  cy.get('input[name="password"]').type(password)
  cy.get('button[type="submit"]').click()
  
  // Vérifie que la connexion a réussi
  cy.url().should('include', '/dashboard')
  // ou vérifie un élément spécifique de ton dashboard
  cy.contains('Bienvenue').should('be.visible')
})

// Version alternative si cy.session ne fonctionne pas
Cypress.Commands.add('loginWithSession', (username: string, password: string) => {
  cy.session([username, password], () => {
    cy.visit('/login')
    cy.get('input[name="username"]').type(username)
    cy.get('input[name="password"]').type(password)
    cy.get('button[type="submit"]').click()
    cy.url().should('include', '/dashboard')
  })
})

